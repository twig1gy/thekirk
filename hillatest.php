<div id="peacepark" class="section para community" style="background-image: url(http://watts-web.com/kirk/wp-content/uploads/2017/12/Kirk_Community_1_colors_green_F.jpg);" data-bottom-top="background-position: 50% 100px;" data-top-bottom="background-position: 50% -100px;">
    <div class="container">
      <div class="row">
        <div class="twelve columns">
          <h2>Peace Park</h2>
        </div>
        <div class="twelve columns">
          <span style="font-weight: 400;">Our spot at 114</span><span style="font-weight: 400;">th</span><span style="font-weight: 400;"> and Wornall has been our home for more than six decades. We love our building and grounds, even as we know it is just a spot for us to gather. But it is more than that: a tool for ministry and mission, a place for us to worship, to give thanks, to say goodbye and to mark transitions. We hope it has shaped our faith and roots us in the South Kansas City community.</span>

<span style="font-weight: 400;">Around 2012, we decided to reshape our memorial grounds and rename our outdoor areas </span><i><span style="font-weight: 400;">The Kirk Peace Park</span></i><span style="font-weight: 400;">. The Peace Park was dedicated in 2014. It continues to be a place for us to celebrate and remember friends and family, but it is also open to the public, where neighbors walking by might connect to something different. If you stop by The Kirk, we hope you have a chance to meander around a bit. Here are some highlights:</span>

<strong>The Peace Portal</strong>

<span style="font-weight: 400;">This brick Peace Plaza was built in 2008 after the retirement of longtime Pastor Clayton Cook.  The pergola was added as part of the 2013 memorial walk installation, and at this time the name Peace Portal was adopted to highlight the entire structure's role as an entrance into the sacred space of the memorial walk along with its function of imparting a feeling of quietude. The words of Romans 12:18 are inscribed around the inside of the pergola : </span><i><span style="font-weight: 400;">If it is possible, so far as it depends on you, live peaceably with all.</span></i>

<strong>The Peace Pole</strong>

<span style="font-weight: 400;">More than 100,000 peace poles have been placed around the world, each declaring “May Peace Prevail on Earth.” Ours was installed in 2016 as an Eagle Scout service project, and repeats this prayer for peace in English, Spanish, Hebrew and Arabic.</span>

<strong>Path of the Spirit (Sculpture)</strong>

<span style="font-weight: 400;">Our Peace Park sculpture was commissioned for The Kirk and depicts three birds in peaceful dance. According to the artist, the thought behind the sculpture is the concept of inspiration as a gift from God through the Holy Spirit. </span>

<strong>The Labyrinth</strong>

<span style="font-weight: 400;">This off-the-trail maze sets apart from the rest of the park, a symbol of thoughtful meditation and repose. While the history of labyrinths predate recorded history, Christians have been walking their paths as a devotional and spiritual practice for centuries.   </span>

<span style="font-weight: 400;">For more information about the memorial components of The Kirk Peace Park, please see this brochure.</span>        </div>
